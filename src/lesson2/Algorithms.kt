/**
 * Наибольшая общая подстрока.
 * Средняя
 *
 * Дано две строки, например ОБСЕРВАТОРИЯ и КОНСЕРВАТОРЫ.
 * Найти их самую длинную общую подстроку -- в примере это СЕРВАТОР.
 * Если общих подстрок нет, вернуть пустую строку.
 * При сравнении подстрок, регистр символов *имеет* значение.
 * Если имеется несколько самых длинных общих подстрок одной длины,
 * вернуть ту из них, которая встречается раньше в строке first.
 */
fun longestCommonSubstring(first: String, second: String): String {
    /*
    n,m - words lengths
        time complexity: O(n*m)
        space complexity: O(n)
     */

    val lens = Array(2) { Array(second.length) { 0 } }
    var maxLen = 0
    var maxLenIdx = -1

    var current = 0


    for (i in first.indices) {
        for (j in second.indices) {
            val currentCharSame = (first[i] == second[j])

            if (currentCharSame) {
                lens[current][j] = if (i == 0 || j == 0) 1 else lens[1 - current][j - 1] + 1

                if (lens[current][j] > maxLen) {
                    maxLen = lens[current][j]
                    maxLenIdx = i
                }
            } else {
                lens[current][j] = 0
            }
        }

        current = 1 - current
    }

    return if (maxLenIdx == -1) "" else first.substring(maxLenIdx - maxLen + 1..maxLenIdx)

}

/**
 * Число простых чисел в интервале
 * Простая
 *
 * Рассчитать количество простых чисел в интервале от 1 до limit (включительно).
 * Если limit <= 1, вернуть результат 0.
 *
 * Справка: простым считается число, которое делится нацело только на 1 и на себя.
 * Единица простым числом не считается.
 */
fun calcPrimesNumber(limit: Int): Int {
    /*
        time complexity: O(n log(log n))
        space complexity: O(n)
     */

    val sieve = BooleanArray(limit + 1) { true }
    // skip and forget [0], [1], [2 + 2k, k >= 1
    val upper = ceil(sqrt(limit.toDouble())).toInt()
    for (i in 3..upper step 2) { // just optimization to skip all evens
        if (sieve[i]) {
            for (j in (i * i)..limit step i)
                sieve[j] = false
        }
    }

    //     take all primes from 3 to limit skipping evens, then plus 1 (which means prime number 2) if limit >= 2
    return sieve.slice(3..limit step 2).count { it }.let { if (limit >= 2) it + 1 else it }
}
